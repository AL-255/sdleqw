/* The fallback glyph: used when char not defined. */
static const glyph_t fallback_stack = { 5, 7, 6, 5, 3, fs_blank };
static const glyph_t fallback_mini  = { 4, 6, 5, 4, 2, fm_blank };

/* Convert a `gent` table entry to glyph_t lazily (gent and glyph_t share layout). */
const glyph_t *font_stack(int ch)
{
    static glyph_t buf;
    unsigned u = (unsigned)ch & 0xff;
    /* Prefer HP-extracted glyph if present (variable width). */
    if (u < 128 && hp_glyph_table[u].rows) {
        const hp_glyph_t *h = &hp_glyph_table[u];
        buf.w = h->w; buf.h = 7; buf.advance = h->advance;
        buf.baseline = 5; buf.axis = 3; buf.rows = h->rows;
        return &buf;
    }
    const gent *g = &stack_table[u];
    if (!g->rows) return &fallback_stack;
    buf.w = g->w; buf.h = g->h; buf.advance = g->advance;
    buf.baseline = g->baseline; buf.axis = g->axis; buf.rows = g->rows;
    return &buf;
}

const glyph_t *font_mini(int ch)
{
    static glyph_t buf;
    unsigned u = (unsigned)ch & 0xff;
    const gent *g = &mini_table[u];
    if (!g->rows) return &fallback_mini;
    buf.w = g->w; buf.h = g->h; buf.advance = g->advance;
    buf.baseline = g->baseline; buf.axis = g->axis; buf.rows = g->rows;
    return &buf;
}

int font_draw_stack(bitmap_t *bm, int x, int y, const char *s, int v)
{
    int x0 = x;
    while (*s) {
        const glyph_t *g = font_stack((unsigned char)*s++);
        bm_blit_glyph(bm, x, y, g->rows, g->w, g->h, v);
        x += g->advance;
    }
    return x - x0;
}

int font_draw_mini(bitmap_t *bm, int x, int y, const char *s, int v)
{
    int x0 = x;
    while (*s) {
        const glyph_t *g = font_mini((unsigned char)*s++);
        bm_blit_glyph(bm, x, y, g->rows, g->w, g->h, v);
        x += g->advance;
    }
    return x - x0;
}

int font_measure_stack(const char *s)
{
    int w = 0;
    while (*s) w += font_stack((unsigned char)*s++)->advance;
    return w;
}

int font_measure_mini(const char *s)
{
    int w = 0;
    while (*s) w += font_mini((unsigned char)*s++)->advance;
    return w;
}

/* ============================================================
 * Procedural tall glyphs that scale with content height.
 * All placed at (x, y_top), occupy `height` rows.
 * ============================================================ */

void glyph_draw_paren_l(bitmap_t *bm, int x, int y_top, int height, int v)
{
    if (height <= STACK_FONT_H) {
        const glyph_t *g = font_stack('(');
        bm_blit_glyph(bm, x, y_top + (height - g->h) / 2, g->rows, g->w, g->h, v);
        return;
    }
    /* Curved tall paren in 3-px slot: two tips + vertical body */
    int top = y_top, bot = y_top + height - 1;
    bm_set(bm, x + 1, top, v);
    bm_set(bm, x, top + 1, v);
    for (int y = top + 2; y < bot - 1; y++) bm_set(bm, x, y, v);
    bm_set(bm, x, bot - 1, v);
    bm_set(bm, x + 1, bot, v);
}

void glyph_draw_paren_r(bitmap_t *bm, int x, int y_top, int height, int v)
{
    if (height <= STACK_FONT_H) {
        const glyph_t *g = font_stack(')');
        bm_blit_glyph(bm, x, y_top + (height - g->h) / 2, g->rows, g->w, g->h, v);
        return;
    }
    int top = y_top, bot = y_top + height - 1;
    bm_set(bm, x, top, v);
    bm_set(bm, x + 1, top + 1, v);
    for (int y = top + 2; y < bot - 1; y++) bm_set(bm, x + 1, y, v);
    bm_set(bm, x + 1, bot - 1, v);
    bm_set(bm, x, bot, v);
}

int glyph_paren_width(int height)
{
    if (height <= STACK_FONT_H) return font_stack('(')->advance;
    return 3;
}

